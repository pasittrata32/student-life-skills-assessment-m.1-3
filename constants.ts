const parseStudents = (rawList: string[], room: string, startId: number): Student[] => {
  return rawList.map((fullName, index) => {
    const parts = fullName.trim().split(/\s+/);
    // Handle cases where prefix might be separated or attached.
    // Assuming simple format "Prefix Name Surname" or "Prefix Name Middle Surname"
    let prefix = parts[0];
    let firstName = parts[1];
    let lastName = parts.slice(2).join(" ");

    return {
      id: startId + index,
      no: index + 1,
      prefix,
      firstName,
      lastName,
      room
    };
  });
};

